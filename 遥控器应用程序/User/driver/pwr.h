/**电源转换IC LTC3440**/
#define	Ltc3440BurstModeDisabled()		GPIO_ResetBits(LTC3440_MODE_PORT, LTC3440_MODE_PIN);//突变模式禁止
#define	Ltc3440BurstModeEnabled()		GPIO_SetBits(LTC3440_MODE_PORT, LTC3440_MODE_PIN);//突变模式使能

/**OLED供电相关**/
#define	OledChipPwrOn()			GPIO_ResetBits(GPIOB, GPIO_Pin_0);//接收使能
